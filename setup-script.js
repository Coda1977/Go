async function main() {
  console.clear();
  colorLog('cyan', 'ðŸš€ Go Leadership App Setup (PostgreSQL)');
  colorLog('cyan', '=======================================\n');
  
  try {
    // Welcome message
    colorLog('blue', 'Welcome! This script will help you set up your Go Leadership App with PostgreSQL.');
    colorLog('yellow', 'Make sure you have Node.js 18+ and npm installed.\n');
    colorLog('magenta', 'ðŸ˜ This version uses PostgreSQL 15 - perfect for Replit deployment!\n');
    
    const proceed = await askQuestion('â“ Continue with setup? (Y/n): ');
    if (proceed.toLowerCase() === 'n') {
      colorLog('yellow', 'ðŸ‘‹ Setup cancelled');
      process.exit(0);
    }
    
    // Create directories
    colorLog('cyan', '\nðŸ“ Creating directories...');
    await createDirectories();
    
    // Create migrations directory
    if (!fs.existsSync('migrations')) {
      fs.mkdirSync('migrations', { recursive: true });
      colorLog('green', 'âœ… Created directory: migrations');
    } else {
      colorLog('yellow', 'âšª Directory already exists: migrations');
    }
    
    // Create .env file
    await createEnvFile();
    
    // Install dependencies
    await installDependencies();
    
    // Validate setup
    const setupValid = await validateSetup();
    if (!setupValid) {
      colorLog('red', '\nâŒ Setup validation failed');
      process.exit(1);
    }
    
    // Run database migrations
    const migrationsRun = await runMigrations();
    if (!migrationsRun) {
      colorLog('yellow', '\nâš ï¸  Migration failed - you may need to run them manually later');
    }
    
    // Test configuration
    const configValid = await testConfiguration();
    if (!configValid) {
      colorLog('red', '\nâŒ Configuration test failed');
      colorLog('yellow', 'ðŸ’¡ Please check your .env file and DATABASE_URL');
      colorLog('yellow', 'ðŸ’¡ On Replit, DATABASE_URL is provided automatically');
      process.exit(1);
    }
    
    // Generate startup script
    await generateStartupScript();
    
    // Show completion message
    await showCompletionMessage();
    
  } catch (error) {
    colorLog('red', `\nâŒ Setup failed: ${error.message}`);
    process.exit(1);
  } finally {
    rl.close();
  }
}

