async function installDependencies() {
  colorLog('cyan', '\nðŸ“¦ Checking dependencies...');
  
  if (!fs.existsSync('node_modules')) {
    const install = await askQuestion('â“ Install dependencies with npm? (Y/n): ');
    if (install.toLowerCase() !== 'n') {
      colorLog('blue', 'ðŸ“¥ Installing dependencies...');
      
      const { spawn } = require('child_process');
      
      return new Promise((resolve, reject) => {
        const npm = spawn('npm', ['install'], { stdio: 'inherit' });
        
        npm.on('close', (code) => {
          if (code === 0) {
            colorLog('green', 'âœ… Dependencies installed successfully');
            resolve();
          } else {
            colorLog('red', 'âŒ Failed to install dependencies');
            reject(new Error('npm install failed'));
          }
        });
      });
    }
  } else {
    colorLog('green', 'âœ… Dependencies already installed');
  }
}

async function testConfiguration() {
  colorLog('cyan', '\nðŸ§ª Testing configuration...');
  
  // Load environment variables
  require('dotenv').config();
  
  // Test OpenAI API Key
  if (process.env.OPENAI_API_KEY) {
    colorLog('green', 'âœ… OpenAI API Key configured');
  } else {
    colorLog('red', 'âŒ OpenAI API Key missing');
    return false;
  }
  
  // Test email configuration
  if (process.env.EMAIL_USER && process.env.EMAIL_PASSWORD) {
    colorLog('green', 'âœ… Email configuration found');
  } else {
    colorLog('red', 'âŒ Email configuration incomplete');
    return false;
  }
  
  // Test PostgreSQL database connection
  if (process.env.DATABASE_URL) {
    colorLog('green', 'âœ… DATABASE_URL configured');
    try {
      const { testConnection } = require('./database');
      const connected = await testConnection();
      if (connected) {
        colorLog('green', 'âœ… PostgreSQL connection successful');
      } else {
        colorLog('red', 'âŒ PostgreSQL connection failed');
        return false;
      }
    } catch (error) {
      colorLog('red', `âŒ PostgreSQL connection test failed: ${error.message}`);
      return false;
    }
  } else {
    colorLog('yellow', 'âš ï¸  DATABASE_URL not set - this is normal on Replit');
    colorLog('blue', 'ðŸ’¡ Replit will provide DATABASE_URL automatically when deployed');
  }
  
  // Test database initialization
  try {
    const { initDatabase } = require('./database');
    await initDatabase();
    colorLog('green', 'âœ… Database initialized successfully');
  } catch (error) {
    colorLog('red', `âŒ Database initialization failed: ${error.message}`);
    colorLog('yellow', 'ðŸ’¡ Try running: npm run migrate up');
    return false;
  }
  
  return true;
}

